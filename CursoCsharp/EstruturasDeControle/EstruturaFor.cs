using System;

namespace CursoCsharp.EstruturasDeControle
{
    class EstruturaFor
    {
        public static void Executar()
        {
            // Iniciando com while
            int i = 1;

            while(i <= 10)
            {
                Console.WriteLine($"O valor de i é {i}.");
                i++;
            }
        }
    }
}
